// overriding:-Method overriding occurs in dart when a child 
// class tries to override the parent class's method. 

// How to override:
//  overriding method (the child class method)
//   must be declared with the same configuration
//   as the overridden method (the superclass method).
//    The return type, list of arguments and its sequence
//     must be the same as the parent class method.